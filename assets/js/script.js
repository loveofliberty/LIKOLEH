// ---------------------------
// TRANSLATION HELPERS
// ---------------------------

const LANG_STORAGE_KEY = "likoleh_lang"
let flagsBound = false

function setLanguage(lang){
  const fallback = translations.en || {}
  const dict = translations[lang] || fallback

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n")
    const value = dict[key] ?? fallback[key]
    if (value) {
      el.textContent = value
    }
  })

  try{
    localStorage.setItem(LANG_STORAGE_KEY, lang)
  }catch(e){
    // ignore storage errors (e.g., private mode)
  }
}

function bindFlagClicks(){
  if (flagsBound) return

  const flags = document.querySelectorAll(".flags img, .language-switcher img, img#lang-en, img#lang-fr, img#lang-de")
  if (!flags.length) return

  flags.forEach(img => {
    const src = img.getAttribute("src") || ""
    if (src.includes("gb.svg")) {
      img.addEventListener("click", () => setLanguage("en"))
    }
    if (src.includes("fr.svg")) {
      img.addEventListener("click", () => setLanguage("fr"))
    }
    if (src.includes("de.svg")) {
      img.addEventListener("click", () => setLanguage("de"))
    }
  })

  flagsBound = true
}

function initLanguage(){
  bindFlagClicks()
  const stored = (() => { try { return localStorage.getItem(LANG_STORAGE_KEY) } catch(e) { return null } })()
  setLanguage(stored || "en")
}

document.addEventListener("layoutLoaded", initLanguage)
document.addEventListener("DOMContentLoaded", initLanguage)


